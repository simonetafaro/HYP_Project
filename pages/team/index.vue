import DoubleColorTitle from '~/components/utils/DoubleColorTitle.vue'

export default {
  components: {
    MemberMini,
    DoubleColorTitle,
  },

  mixins: [GoToMixins],
  async asyncData({ $axios }) {
    const { data } = await $axios.get(`${process.env.BASE_URL}/api/teammembers`)
    const people = data
    const areasData = await $axios.get(`${process.env.BASE_URL}/api/areas`)
    const areas = areasData.data

    return {
      people,
      areas,
    }
  },
  data() {
    return {
      selectedArea: null,
      member: {},
      memberIndex: null,
      relCasestudies: {},
      carouselList: [],
      areaMenuHidden: true,
    }
  },
  methods: {
    async filterTeamMemberByArea(e, areaID) {
      this.people = await this.$axios.$get(
        `${process.env.BASE_URL}/api/teammembersbyarea/${areaID}`
      )
      const filters = e.target.parentNode.children
      filters.forEach((filter) => {
        filter.classList.remove('active-filter')
      })
      const element = e.target
      element.classList.add('active-filter')
      this.selectedArea = await this.$axios.$get(
        `${process.env.BASE_URL}/api/area/${areaID}`
      )
      this.selectedArea = this.selectedArea.title
    },
    async findAllTeamMembers(e) {
      this.people = await this.$axios.$get(
        `${process.env.BASE_URL}/api/teammembers`
      )
      const filters = e.target.parentNode.children
      filters.forEach((filter) => {
        filter.classList.remove('active-filter')
      })
      const element = e.target
      element.classList.add('active-filter')
      this.selectedArea = null
    },

    scrollRight(memberID) {
      this.showOverlay(memberID + 1, this.memberIndex + 1)
    },

    scrollLeft(memberID) {
      this.showOverlay(memberID - 1, this.memberIndex - 1)
    },

    DropdownArea() {
      const width =
        window.innerWidth ||
        document.documentElement.clientWidth ||
        document.body.clientWidth
      if (width < 1200) {
        const areaFilters = document.getElementsByClassName('filter')
        const areaArrow = document.getElementsByClassName('area-dropdown-arrow')
        if (this.areaMenuHidden) {
          for (let i = 0; i < areaFilters.length; i++) {
            areaFilters[i].style.display = 'list-item'
          }
          areaArrow[0].style.transform = 'initial'
          this.areaMenuHidden = false
        } else {
          for (let i = 0; i < areaFilters.length; i++) {
            areaFilters[i].style.display = 'none'
          }
          areaArrow[0].style.transform = 'rotate(-90deg)'
          this.areaMenuHidden = true
        }
      }
    },
  },
  mounted() {
    this.resizeCaseCard()
    window.addEventListener('resize', this.resizeCaseCard)
  },
  destroyed() {
    window.removeEventListener('resize', this.resizeServiceCard)
    window.removeEventListener('resize', this.resizeCaseCard)
  },
}
