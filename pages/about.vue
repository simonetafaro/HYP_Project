  },

  head() {
    return {
      title: 'HexTech - About',
    }
  },

  mounted() {
    const carousel = document.querySelector("[data-target='carousel']")

    const carouselWidth = carousel.offsetWidth
    const cardMarginRight = 30
    const cardCount = carousel.querySelectorAll("[data-target='card']").length
    const maxX = -(
      cardCount * carouselWidth +
      cardMarginRight * cardCount -
      carouselWidth -
      cardMarginRight -
      (carouselWidth + cardMarginRight) * 2
    )
    this.carouselProp = {
      offset: 0,
      carouselWidth,
      cardMarginRight,
      maxX,
    }
  },

  methods: {
    scrollRight() {
      const carousel = document.querySelector("[data-target='carousel']")

      if (this.carouselProp.offset > this.carouselProp.maxX) {
        this.carouselProp.offset -=
          this.carouselProp.carouselWidth + this.carouselProp.cardMarginRight
        carousel.style.transform = `translateX(${
          this.carouselProp.offset / 3
        }px)`
      }
    },
    scrollLeft() {
      const carousel = document.querySelector("[data-target='carousel']")
      if (this.carouselProp.offset !== 0) {
        this.carouselProp.offset +=
          this.carouselProp.carouselWidth + this.carouselProp.cardMarginRight
        carousel.style.transform = `translateX(${
          this.carouselProp.offset / 3
        }px)`
      }
    },
  },
}
</script>

